package modelo;

/**
 *
 * @author sinNombre
 */
public class Comentario {
    String id;
    String nick;
    String comentario;

    public Comentario(String id, String nick, String comentario) {
        this.id = id;
        this.nick = nick;
        this.comentario = comentario;
    }
    

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNick() {
        return nick;
    }

    public void setNick(String nick) {
        this.nick = nick;
    }

    public String getComentario() {
        return comentario;
    }

    public void setComentario(String comentario) {
        this.comentario = comentario;
    }
    
    
}
